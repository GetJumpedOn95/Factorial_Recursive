func factorial(num:Int) -> Int {
    print("Calculating factorial(\(num))...")
    if num == 1 {
        print("Terminal case, returning 1.")
        return 1
    }else{
        let result =  num * factorial(num: num - 1)
        print("Non-terminal case, returning \(result).")
        return result
    }
}

let result = factorial(num:n)
